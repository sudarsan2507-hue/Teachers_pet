def generate_session_inference(session_code: str) -> str:
    """Aggregates session data and generates an AI summary/inference via Groq."""
    session = get_session(session_code)
    if not session or getattr(config, "groq_client", None) is None:
        return "Inference unavailable."

    submissions = get_submissions(session_code)
    if not submissions:
        return "No submissions to analyze."

    topic = session["topic"]
    question = session["question"]
    
    # Prep data for AI
    results_summary = ""
    for s in submissions:
        results_summary += f"- Student: {s['student_name']}, Score: {s['score']}%, Status: {s['status']}, Reason: {s['reason']}\n"

    prompt = (
        f"Topic: {topic}\n"
        f"Question asked: {question}\n\n"
        f"Student Performance Data:\n{results_summary}\n"
        "Task: Based on these results, provide a concise pedagogical inference for the teacher.\n"
        "1. Identify common conceptual gaps or patterns in mistakes.\n"
        "2. Suggest an alternate teaching method, analogy, or mental model that would help students master this specific topic better.\n"
        "3. Tone: Be highly supportive, professional, and collaborative. Do NOT criticize or undermine the teacher's current method; instead, offer the suggestion as a supplemental or 'next step' strategy.\n"
        "4. Format: Use Markdown. Start with a header '### 💡 Session Insights' followed by '#### Analysis' and '#### Teaching Suggestion'."
    )

    try:
        chat_completion = config.groq_client.chat.completions.create(
            messages=[
                {"role": "system", "content": "You are an expert pedagogical advisor and mentor for computer science teachers. Your goal is to improve student outcomes through supportive, data-driven feedback."},
                {"role": "user", "content": prompt}
            ],
            model="llama-3.1-8b-instant",
            temperature=0.7,
            max_completion_tokens=400,
        )
        inference_text = chat_completion.choices[0].message.content.strip()
        database.update_session_inference(session_code, inference_text)
        return inference_text
    except Exception as e:
        print(f"Groq inference generation failed: {e}")
        return "Failed to generate AI inference due to an API error."
